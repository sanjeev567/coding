/*
stl have three components 
    1>  containers    -->stores data using template;
    2>  algorithms    -->searching, sorting using template functions
    3>  iterators     --> object points to an elements of the containers (same as pointer ) 
                      --> connects  algorithms with containers 

    STL == containers + algorithms + iterators


****  Containers  -->are of three types
        1> sequence         -->Linear fashion
            * vectors
            * list
            * dequeue
        2> associative      -->Direct access
            * set
            * multiset
            * map
            * multimap
        3> Derived containers   -->real world modelling
            * stack
            * queue
            * priority queue


***     <-------------------------------SEQUENCE CONTAINERS-------------->

    * Vector
    1> Random Access    ---> Fast
    2> Insertion and deletion in the middle      ---> slow
    3> Insertion and deletion at the end         ---> Fast
    
    * list
    1> Random Access    ---> slow
    2> Insertion and deletion in the middle      ---> fast
    3> Insertion and deletion at the end         ---> Fast


***  <--------------Associative containers----------->
    --> All operation are fast except Random Access

***  <--------------Derived containers--------------->

    depends on data structures




*/